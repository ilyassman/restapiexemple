            <h1>Présentation: 
            </h1>
            
<p>Bienvenue dans notre formation complète en Génie Civil, conçue
  pour vous initier et vous perfectionner dans ce domaine crucial pour la construction 
  et l'aménagement de notre environnement. Que vous soyez débutant(e) ou que vous souhaitiez 
  renforcer vos compétences existantes, cette formation vous offre une immersion 
  approfondie dans les techniques et les concepts fondamentaux du Génie Civil.
</p>
  <h1>À qui s'adresse cette formation <i class="fas fa-users"></i>
  </h1>        
  <p>Cette formation s'adresse à tous les passionnés du Génie Civil, des étudiants en quête d'une carrière enrichissante dans ce secteur aux professionnels 
    désirant actualiser leurs connaissances et acquérir de nouvelles compétences.</p>
     <h1>Prérequis <i class="fas fa-check-circle"></i>
    </h1>        
    <p>Aucune connaissance préalable en Génie Civil n'est requise, mais une familiarité avec les mathématiques et la physique serait un atout.</p>
<h1>Objectifs <i class="fas fa-bullseye"></i></h1>
<p> À la fin de cette formation, vous serez capable de :
  <br>
  <ul>
    <li>Comprendre les principes fondamentaux du Génie Civil et ses différentes branches.</li>
  <li>Maîtriser les techniques de conception et de construction des ouvrages d'art, tels que les ponts, les bâtiments et les routes.</li>
  <li>Analyser et dimensionner les structures en béton armé et en acier.</li>
  <li>Gérer et suivre les projets de construction dans le respect des normes et des réglementations.</li>
  <li>Appliquer les principes du développement durable dans les projets de Génie Civil.</li>
  <li>Utiliser les logiciels de simulation et de calcul pour optimiser les projets.</li>
  <li>Travailler en équipe et communiquer efficacement avec les différents acteurs du chantier.</li>
  </ul>
  </p>

  <h1> Programme de la formation <i class="fas fa-list-ul"></i></h1>
  <p> <h3><i class="fas fa-code"></i> Module 1 : Fondamentaux du Génie Civil</h3>
    <ul>
      <li>Introduction au Génie Civil et ses domaines d'application.</li>
      <li>Mécanique des matériaux et résistance des structures.</li>
      <li>Géotechnique et hydraulique.</li>
      <li>Topographie et géomatique.</li>
      <li>Mathématiques et informatique appliquées au Génie Civil.</li>
    </ul>
    
     <!-- Module 3 -->
<h3><i class="fas fa-code-branch"></i> Module 3 : Conception et Dimensionnement</h3>
<ul>
  <li>Analyse et dimensionnement des structures.</li>
  <li>Calcul des charges et des efforts.</li>
  <li>Application des normes et réglementations.</li>
  <li>Utilisation des logiciels de simulation et de calcul.</li>
</ul>

<!-- Module 4 -->
<h3><i class="fas fa-tasks"></i> Module 4 : Management de Projet</h3>
<ul>
  <li>Gestion de projet et planification.</li>
  <li>Suivi et contrôle des coûts.</li>
  <li>Communication et coordination des équipes.</li>
  <li>Sécurité et prévention des risques.</li>
</ul>

<!-- Module 5 -->
<h3><i class="fas fa-leaf"></i> Module 5 : Développement Durable</h3>
<ul>
  <li>Enjeux du développement durable dans le Génie Civil.</li>
  <li>Eco-conception et choix des matériaux durables.</li>
  <li>Performance énergétique des bâtiments.</li>
  <li>Gestion des déchets et des ressources naturelles.</li>
</ul>

<!-- Module 6 -->
<h3><i class="fas fa-plus-circle"></i> Et plus encore...</h3>
<ul>
  <li>Projets pratiques et études de cas concrets.</li>
  <li>Interventions de professionnels du Génie Civil.</li>
  <li>Visites de chantiers et d'ouvrages d'art.</li>
  <li>Possibilité de stage en entreprise.</li>
</ul>

<h1>Débouchés: 
</h1>

<p>Le Génie Civil offre un large éventail de débouchés dans les secteurs public et privé. Les ingénieurs en Génie Civil peuvent travailler dans les bureaux d'études, les entreprises de construction, les administrations publiques, les organismes de recherche et d'enseignement, etc.
</p>

